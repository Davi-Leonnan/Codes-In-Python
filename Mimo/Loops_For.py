# A função "loops for" cria loops muitos mais dinâmicos e fáceis de entender.

# A variável "in" (i) é a variável contadora, a qual mostra em qual posição a contagem está no momento.

# Exemplo 01:

for i in range(5):
    print(i)
    print("The cake is a lie")

for i in range(5):
    print(i)
    print("Don't trust in Glados!")

