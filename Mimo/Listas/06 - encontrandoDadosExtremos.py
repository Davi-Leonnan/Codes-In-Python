# Para encontrar o maior valor em uma lista, utilizamos a função "max" como o nome da lista entre parenteses.

# Exemplo 01:

grades = [9.5, 8.7, 4.5, 7.6, 6.8, 9.8]

print(max(grades))


# Para encontrar o menor valor em uma lista, utilizamos a função "min" como o nome da lista entre parenteses.

# Exemplo 02:

Montantes = [14000, 12000, 13500, 8000, 17800, 20050,]

print(min(Montantes))

# Exemplo 03:

Amostras = [2.1, 4.3, 5.3, 3.7, 7.2, 3.9]

Menor_Amostra = min(Amostras)

print(Menor_Amostra)


