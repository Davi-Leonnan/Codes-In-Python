# Cada item da item tem uma posição numerada chamada de "índice".

# Os índices começam do "0" e vão até a última posição em que o último elemento da lista se encontra.

# Exemplo:

left4DeadCharacters = ['Bill', 'Francis', 'Zoey', 'Louie']

print(left4DeadCharacters[2])


left4Dead2Characters = ["Coach", "Nick", "Ellis", "Rochelle"]

left4Dead2Characters[2] = "Gnomo"

print(left4Dead2Characters)


