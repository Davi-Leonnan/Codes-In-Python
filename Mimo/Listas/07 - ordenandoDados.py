# Caso queiramos ordenar valores de uma lista do menor pro maior, utilizamos a função ".sort()".

# Exemplo 01:

resultados = [7, 8, 4.5, 9.7, 7.5, 5, 6, 8.5, 10, 9, 6.8]

resultados.sort()

print(resultados)

# Exemplo 02:

gods = ["Zeus", "Poseidon", "Hades", "Ares", "Hera", "Dermete", "Athena", "Hermes", "Apollo", "Artemis", "Herfesto"]

gods.sort()

print(gods)
