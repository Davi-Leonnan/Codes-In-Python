# Caso desejamos iterar todos os elementos de uma lista, utilizamos a função "for" junto de uma variável.

# Exemplo 01:

smiling_friends = ["Pim", "Charlie", "Alan", "Glep"]

for friends in smiling_friends:
    print(friends)
    print("---")

# Exemplo 02:

park_workers = ["Mordecai", "Rigby", "Saltitão", "Pairulito", "Musculoso", "Fantasmão", "Benson"]

for workers in park_workers:
    print(workers + " employer")
    print("---")

