# Soma de números:

def addNumbers(num1, num2):
    sum = num1 + num2
    print(sum)
    return

addNumbers(2,3)
addNumbers(5,4)

# Subtração de numeros:

def minNumbers(num1, num2):
    mim = num1 - num2
    print(mim)
    return

minNumbers(10,5)
