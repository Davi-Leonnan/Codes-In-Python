# Importação de Operação Com Números:

import math

pi = 3.14

x = 1

y = 2

z = 3

print(round(pi))  # Oferece a aproximação de um número não inteiro.

print(math.ceil(pi))  # Oferece uma aproximação maior de um número não inteiro.

print(math.floor(pi))  # Oferece uma aproximação menor de um número não inteiro.

print(abs(pi))  # Converte um número negativo para um número positivo.

print(pow(pi,2)) # Múltiplica o número pelo mesmo valor adicionado.

print(math.sqrt(4))  # Oferece a raiz quadrada do número oferecido.

print(max(x,y,z))  # Fornece o maior valor das variáveis seelecionadas.

print(min(x,y,z))  # Fornece o menor valor das variáveis seelecionadas.
