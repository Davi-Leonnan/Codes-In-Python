# Funções -  Chamar uma função somente quando é solicitada:


# Caso queira adiconar mais de um nome, defina dois parâmetros dentro do parentese.

def hello(first_name, last_name, age):  # Ao querer adicionar um valor de entrada para a função, defina o nome do parâmetro dentro do parentese e o adioione na função "print".
    print("hello " + first_name + " " + last_name)
    print("You are " + str(age) + " years old")  # Uma string de número sempre irá requerer a função "str".
    print("Have a nice day!")  # Uma função sempre começa com "def" e requer parenteses no nome da função.

# my_name = "Davi"

hello("Ednaldo", "Pereira", 51)



